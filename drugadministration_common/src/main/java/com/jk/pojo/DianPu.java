package com.jk.pojo;

public class DianPu {
    private Integer id;
    private String miaohsu;
    private String taidu;
    private String sudu;
    private String phone;
    private Integer count;
    private String area;
    private String chanpin;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getMiaohsu() {
        return miaohsu;
    }

    public void setMiaohsu(String miaohsu) {
        this.miaohsu = miaohsu;
    }

    public String getTaidu() {
        return taidu;
    }

    public void setTaidu(String taidu) {
        this.taidu = taidu;
    }

    public String getSudu() {
        return sudu;
    }

    public void setSudu(String sudu) {
        this.sudu = sudu;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getChanpin() {
        return chanpin;
    }

    public void setChanpin(String chanpin) {
        this.chanpin = chanpin;
    }
}
